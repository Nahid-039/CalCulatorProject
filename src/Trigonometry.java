
public class Trigonometry extends CalCulator
{
	public void SinOperation()
	{
		System.out.println("SineOperation: "+Math.sin(x));
	}
	public void CosOperation()
	{
		System.out.println("CosOperation: "+Math.cos(x));
	}
	public void TanOperation()
	{
		System.out.println("TanOperation: "+Math.tan(x));
	}
	public void CosecOperation()
	{
		System.out.println("CosecOperation: "+(1/Math.sin(x)));
	}
	public void SecOperation()
	{
		System.out.println("SecOperation: "+(1/Math.cos(x)));
	}
	public void CotOperation()
	{
		System.out.println("CotOperation: "+(1/Math.tan(x)));
	}
	
}
